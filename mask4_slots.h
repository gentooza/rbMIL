static int slotButtonReleasedEvent(PARAM *p, int id, DATA *d)
{
  char texto[100];
	int address;
  if(p == NULL || id == 0 || d == NULL) return -1;
  else if(id == button_return)
{
switch(	MyData.lastMask)
{
	case 1:
	show_mask1(p);
	break;
	case 2: 
	show_mask2(p);
	break;
	case 3: 
	show_mask3(p);
	break;
	case 7: 
	show_mask7(p);
	break;
	default:
	show_mask4(p);
	break;
}
}
 
  else if(id == ackAll)
  {
    myalarm.ackAll();
  }
  else if(id == resetAll)
  {

 	address = wRESET_ALARMAS;
    d->wval[address] = 1;
    sprintf(texto,"register(1,%d)",address);
    adqui.writeIntValue(texto,d->wval[address]);
	address = wRESET_AVISOS;
    d->wval[address] = 1;
    sprintf(texto,"register(1,%d)",address);
    adqui.writeIntValue(texto,d->wval[address]);
    myalarm.resetAll();
    //variables a reinicializar
  }
  return 0;
}

